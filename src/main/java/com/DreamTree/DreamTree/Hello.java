package com.DreamTree.DreamTree;


public class Hello {

    private String hello;
    private String name;

    public Hello() {
    }

    public Hello(String hello, String name) {
        this.hello = hello;
        this.name = name;
    }

    public String getHello() {
        return hello;
    }

    public String getName() {
        return name;
    }
}
